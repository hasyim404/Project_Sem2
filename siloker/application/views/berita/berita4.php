                  Di masa mendatang sejumlah pekerjaan seperti teller bank bakal tergantikan oleh robot. Demikian menurut sebuah laporan yang mengatakan resesi akibat pandemi COVID-19 telah mempercepat perubahan teknologi yang dapat menggantikan 85 juta jenis pekerjaan dalam 5 tahun ke depan.<br><br>
                  "Otomatisasi bersama dengan resesi COVID-19, menciptakan skenario 'gangguan ganda' bagi pekerja," tulis laporan terbaru dari World Economic Forum (WEF) dikutip dari CNN, Rabu (21/10/2020).<br><br>
                  Lebih dari dua perlima perusahaan besar yang disurvei oleh WEF berencana mengurangi tenaga kerja mereka karena adanya integrasi teknologi. <br><br>
                  "Untuk pertama kalinya dalam beberapa tahun terakhir, penciptaan lapangan kerja mulai tertinggal dan faktor ini siap mempengaruhi pekerja yang kurang beruntung," terang WEF dalam laporannya.<br><br>
                  Pandemi virus COVID-19 telah menyebabkan lonjakan tajam pengangguran di seluruh dunia. Beberapa negara ekonomi utama di Eropa dan di tempat lain telah memperpanjang stimulusnya untuk mengimbangi peningkatan pengangguran yang mengkhawatirkan. Pengangguran di Amerika Serikat (AS) juga terus meningkat, sementara anggota parlemen masih berselisih tentang langkah-langkah stimulus baru.<br><br>
                  "Saat angka pengangguran meningkat, hal itu semakin mendesak untuk memperluas perlindungan sosial termasuk dukungan memberikan pelatihan bagi para pekerja yang terlantar dan berisiko saat mereka menavigasi jalan ... menuju 'pekerjaan di masa depan,'" bunyi laporan WEF.<br><br>
                  Pekerja yang tidak dapat bekerja dari rumah, baik karena pekerjaan mereka memerlukan interaksi tatap muka atau karena mereka memiliki akses internet yang terbatas, juga sangat dirugikan. Menurut laporan WEF, mesin atau teknologi dapat menggantikan sekitar 85 juta pekerjaan pada tahun 2025 mendatang, sekaligus memunculkan 97 juta pekerjaan baru. Adapun, pekerjaan yang bakal digantikan oleh mesin seperti asisten administrasi, bookkeepers, dan payroll clerks.<br><br><br>
